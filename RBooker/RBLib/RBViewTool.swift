import UIKit

extension UIView {
    func RBScreenWith() -> CGFloat {
        return UIScreen.main.bounds.width
    }
    
    func RBScreenHeight() -> CGFloat {
        return UIScreen.main.bounds.height
    }
    
    func RBNavigationBarHeight() -> CGFloat {
        return 64
    }
}
